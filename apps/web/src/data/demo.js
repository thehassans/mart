function buildEan13(barcodeBase) {
  const digits = String(barcodeBase || '').replace(/\D/g, '');

  if (digits.length !== 12) {
    throw new Error(`EAN-13 base must contain 12 digits. Received: ${barcodeBase}`);
  }

  const sum = digits.split('').reduce((total, digit, index) => total + Number(digit) * (index % 2 === 0 ? 1 : 3), 0);
  const checkDigit = (10 - (sum % 10)) % 10;

  return `${digits}${checkDigit}`;
}

function buildProductImage(label, accent = '#4f46e5') {
  const displayLabel = String(label || 'Bakala Product')
    .split(' ')
    .slice(0, 3)
    .join(' ');
  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" width="320" height="320" viewBox="0 0 320 320">
      <defs>
        <linearGradient id="g" x1="0" y1="0" x2="1" y2="1">
          <stop offset="0%" stop-color="${accent}"/>
          <stop offset="100%" stop-color="#0f172a"/>
        </linearGradient>
      </defs>
      <rect width="320" height="320" rx="42" fill="url(#g)"/>
      <circle cx="252" cy="68" r="38" fill="rgba(255,255,255,0.16)"/>
      <rect x="34" y="42" width="112" height="112" rx="28" fill="rgba(255,255,255,0.18)"/>
      <text x="34" y="218" fill="#ffffff" font-size="28" font-family="Arial, Helvetica, sans-serif" font-weight="700">${displayLabel}</text>
      <text x="34" y="260" fill="#e2e8f0" font-size="18" font-family="Arial, Helvetica, sans-serif">Saudi Bakala Demo</text>
    </svg>
  `;

  return `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(svg)}`;
}

function createDemoProduct({ barcodeBase, imageAccent = '#4f46e5', ...product }) {
  return {
    ...product,
    barcode: buildEan13(barcodeBase),
    imageUrl: buildProductImage(`${product.brand?.en || ''} ${product.name.en}`.trim(), imageAccent),
  };
}

